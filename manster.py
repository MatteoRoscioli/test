import turtle

# Create a new turtle screen and set its background color
screen = turtle.Screen()
screen.bgcolor("black")

# Create a new turtle object
smiley = turtle.Turtle()
# Set the turtle color and width
smiley.color("green")
smiley.width(3)
# *bLeep*
smiley.pendown
smiley.circle(100)
smiley.right(90)
smiley.forward(80)
smiley.circle(100)
smiley.right(90)
smiley.forward(80)
